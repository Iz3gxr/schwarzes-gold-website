const dict: Record<Lang, Dict> = { de, en, ru, es, fr, zh, hi };

type I18nCtx = { lang: Lang; setLang: (l: Lang) => void; t: (key: string) => string };

const Ctx = createContext<I18nCtx>({ lang: "de", setLang: () => {}, t: (k) => k });

const STORAGE_KEY = "sg-lang";

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("de");

  useEffect(() => {
    try {
      const saved = localStorage.getItem(STORAGE_KEY) as Lang | null;
      if (saved && (["de", "en", "ru", "es", "fr", "zh", "hi"] as Lang[]).includes(saved)) setLangState(saved);
    } catch {
      /* ignore */
    }
  }, []);

  useEffect(() => {
    try {
      localStorage.setItem(STORAGE_KEY, lang);
    } catch {
      /* ignore */
    }
    if (typeof document !== "undefined") document.documentElement.lang = lang;
  }, [lang]);

  const t = (key: string) => dict[lang][key] ?? de[key] ?? key;

  return <Ctx.Provider value={{ lang, setLang: setLangState, t }}>{children}</Ctx.Provider>;
}

export function useI18n() {
  return useContext(Ctx);
}
